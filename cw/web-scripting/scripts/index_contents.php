    <p>
        
        Balmoral Castle has been the Scottish home of the Royal Family since it was purchased for Queen Victoria by Prince Albert in 1852,
        having been first leased in 1848.
     </p>
    
    <p>
        In the autumn of 1842, two and a half years after her marriage to Prince Albert, 
        Queen Victoria paid her first visit to Scotland. They were so struck with the Highlands that they resolved to return. 
        A further visit to Perthshire and then Ardverikie encouraged them to seize the opportunity to purchase Balmoral.
    </p>
    
    <p>
        After searching enquiries they bought the estate on the 17th February 1848 and on 8th September 1848 they 
        arrived to take possession of a property they had never seen, but to which they had committed themselves for many 
        years to come. They were not disappointed and when they returned South they opened negotiations for the purchase of 
        the land on which Balmoral stood.
    </p>
    
    <p>
        These protracted negotiations were completed on 22nd June 1852, when the fee simple of Balmoral was purchased 
        by Prince Albert. Once the land was purchased they decided to rebuild as the building was no longer adequate for
        their needs. The architect selected was William Smith, City Architect of Aberdeen. Soon after the family arrived 
        at the Castle, Mr Smith was summoned from Aberdeen on 8th September 1852.
    </p>
    
    <p>
        Prince Albert decided to build a new Castle as the current one was considered not large enough for the Royal
        Family. A new site was chosen, 100 yards to the North West of the building, so that they could continue to occupy
        the old house while the new Castle was under construction.
     </p>
    
    <p>
        The foundation stone for Balmoral Castle was laid by Queen Victoria on 28th September 1853 and can be found
        at the foot of the wall adjacent to the West face of the entrance porch. Before the foundation stone was placed 
        in position Queen Victoria signed a parchment recording the date. This parchment, together with an example of 
        each of the current coins of the realm, was then placed in a bottle, inserted into a cavity below the site prepared for the stone.
     </p>
    
    <p>
    The Castle was completed in 1856 and the old building was then demolished. This building is commemorated by 
    a stone which is located on the front lawn at a point opposite the tower and about 100 yards from the path. This
     stone marks the position of the front door to the demolished castle.
    </p>
    
    <p>
        When Queen Victoria died in 1901 Balmoral Estates passed, under the terms of her will, to King Edward VII, 
        and from him to each of his successors. Balmoral Estates has been more than just a favourite home to successive 
        generations of the Royal Family.
    </p>
    
    <p>
        Although it remains largely the same as it was in Queen Victoria's reign, successive Royal owners have followed the
        initiative of Prince Albert in making improvements to the estate.
    </p>
    
    <p>
        The Queen, The Duke of Edinburgh and The Prince of Wales take a close personal interest in running and improving the Estates.
    </p>
    
